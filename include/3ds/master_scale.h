/*******************************************************************************
* Classname:	MASTER_SCALE
* Function:		This class defines the scale for all the geometric
*               coordinates in the file.
*******************************************************************************/
class MASTER_SCALE  
{
	public:
		////////////////////////////////////////////////////////////////////////
		// Interface functions
		////////////////////////////////////////////////////////////////////////

		/***********************************************************************
		* Name:			MASTER_SCALE ()
		* Function:		Constructor 
		* Pre:          -
		* Post:         Object is created.
		***********************************************************************/
		MASTER_SCALE ();

		/***********************************************************************
		* Name:			~MASTER_SCALE ()
		* Function:		Destructor
		* Pre:          -
		* Post:         Object is destroyed.
		***********************************************************************/
		virtual ~MASTER_SCALE ();

		/***********************************************************************
		* Name:			Read3DS (int iNrOfBytes, byte* bp3DSData)
		* Function:		This function reads 3ds data.
		* Pre:          bp3DSData contains the 3DS data and iNrOfBytes
		*				contains the number of bytes in the 3DS data.
		* Post:         3DS data is stored in the datastructure.
		***********************************************************************/
		int Read3DS (int iNrOfBytes, byte* bp3DSData);

		/***********************************************************************
		* Name:			GetScale (float* fScale)
		* Function:		This function returns the scale.
		* Pre:          -
		* Post:         fScale contains the scale (units per inch).
		***********************************************************************/
		int GetScale (float* fScale);

		////////////////////////////////////////////////////////////////////////
		// Public variables
		////////////////////////////////////////////////////////////////////////

	private:
		////////////////////////////////////////////////////////////////////////
		// Private functions
		////////////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////////////
		// Private variables
		////////////////////////////////////////////////////////////////////////
		float		fScale;			// Units per inch
}; // End MASTER_SCALE

