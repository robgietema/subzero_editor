int MAT_MAP_BLUR::Read3DS (int iNrOfBytes, byte* bp3DSData)
{
	int iCurrentByte = 0;	// Pointer to current byte in chunk

	// Print status
	VerbosePrintf ("Begin reading MAT_MAP_BLUR chunk");

	// Get version from buffer
	MAT_MAP_BLUR::fBlur = GetFloat (bp3DSData);
	
	// Print status
	VerbosePrintf ("Map blur: %f", MAT_MAP_BLUR::fBlur);
		
	// Print status
	VerbosePrintf ("End reading MAT_MAP_BLUR chunk");

	// Return no error
	return (0);
}
