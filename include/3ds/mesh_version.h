/*******************************************************************************
* Classname:	MESH_VERSION
* Function:		This class is used to describe the version of 3D Studio.
*******************************************************************************/
class MESH_VERSION  
{
	public:
		////////////////////////////////////////////////////////////////////////
		// Interface functions
		////////////////////////////////////////////////////////////////////////

		/***********************************************************************
		* Name:			MESH_VERSION ()
		* Function:		Constructor 
		* Pre:          -
		* Post:         Object is created.
		***********************************************************************/
		MESH_VERSION ();

		/***********************************************************************
		* Name:			~MESH_VERSION ()
		* Function:		Destructor 
		* Pre:          -
		* Post:         Object is destroyed.
		***********************************************************************/
		virtual ~MESH_VERSION ();

		/***********************************************************************
		* Name:			Read3DS (int iNrOfBytes, byte* bp3DSData)
		* Function:		This function reads 3ds data.
		* Pre:          bp3DSData contains the 3DS data and iNrOfBytes
		*				contains the number of bytes in the 3DS data.
		* Post:         3DS data is stored in the datastructure.
		***********************************************************************/
		int Read3DS (int iNrOfBytes, byte* bp3DSData);

		/***********************************************************************
		* Name:			GetVersion (long* lVersion)
		* Function:		This function return the version number.
		* Pre:          -
		* Post:         lVersion contains the version number.
		***********************************************************************/
		int GetVersion (long* lVersion);

		////////////////////////////////////////////////////////////////////////
		// Public variables
		////////////////////////////////////////////////////////////////////////

	private:
		////////////////////////////////////////////////////////////////////////
		// Private functions
		////////////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////////////
		// Private variables
		////////////////////////////////////////////////////////////////////////
		long		lVersion;		// Version of 3D Studio 
}; // End MESH_VERSION

